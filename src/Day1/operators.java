package Day1;
import java.util.Scanner;

public class operators {

    public static void main(String[] args) {

        int x = 10;

        x = x + 1; //x++

        System.out.println(x);

        x= x - 1; //x--

        System.out.println(x);

        x = x*2;

        System.out.println(x);

        x = x/2;

        System.out.println(x);

        x = x%3;

        System.out.println(x);

        double y = 10;
        y = y/3;

        System.out.println(y);


    }
}
